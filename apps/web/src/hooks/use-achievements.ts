export function useAchievementCount(address: `0x${string}` | undefined) {
  return useReadContract({
    address: CONTRACTS.achievements,
    abi: ACHIEVEMENTS_ABI,
    functionName: "achievementCount",
    args: address ? [address] : undefined,
    query: { enabled: !!address },
  });
}

export function useAchievementTokenIds(address: `0x${string}` | undefined) {
  return useReadContract({
    address: CONTRACTS.achievements,
    abi: ACHIEVEMENTS_ABI,
    functionName: "getAchievements",
    args: address ? [address] : undefined,
    query: { enabled: !!address },
  });
}
